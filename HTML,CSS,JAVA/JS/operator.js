// Operator - Symbol or word that changes the value of variable/operand

// Arithmetic operator(+,-,*,/,%,**,++,--)

// 1. Increment (++) - raise value by 1
let x = 10;
let y = 10;
console.log(++x, x);// pre-increment
console.log(y++, y); //post-increment

// 2. Decrement (--) - decrease value by 1
console.log(--x, x);// pre-decrement
console.log(y--, y); //post-decrement


// Comparision Opeartor (>,<,>=,<=,==,===,!=,!==)
// Value Comparision (==,!=)
console.log(5=='5'); //return true if value is same and vice versa
console.log(10!='10'); //return true if value i different and vice versa.

// Value with data type comparision (===,!==) 
